package DAY22Operators;

public class ArithomaticOperation1 {

	public static void main(String[] args) {
		 
		// Variables Definition and Initialization
		
		int number1 = 13;
		int number2 = 4;
		 
		// Addition Operation 
		int res0 = number1  + number2;
		System.out.println(" SUM IS : " + res0);
		
		// Subtraction Operation
	 int  res1 = number1  - number2;
		System.out.println("SUB IS : " + res1);
		
		// Multiplication Operation 
	int	res2 = number1 *  number2;
		System.out.println("MUL IS : " + res2);
		
		// Division Operation
	int 	res3 = number1  / number2;
	System.out.println("Quotient / Division is : " + res3);
	
			
		// Modulus Operation
	int res4 = number1  % number2;
	System.out.println("Remainder/Modulus is : " + res4);
}
}