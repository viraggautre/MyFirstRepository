package DAY24conditionalStatementFOR_IF_loops;

public class ForLoop2ConditionalStatemen {

	public static void main(String[] args) {
		 
		// Print hello for 50 times
		
		for(int i=0; i<=50;  i++) {
			System.out.println(i + ":- Hello");
		}
		
		// for reverse
		
		for(int i=50; i>=0; i--) {
			System.out.println(i+":- hello");
		}
	}
	

}
