package DAY24conditionalStatementFOR_IF_loops;

public class IfEvenNumberProg4 {

	public static void main(String[] args) {
		 
		/*
		 * num%2==0 ---> even else odd
		 */
		int num1=44;
		if(num1%2==0) {
			System.out.println("Number is even");
		}else {
			System.out.println("Number is odd");
		}
	}

}
