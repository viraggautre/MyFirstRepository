package DAY24conditionalStatementFOR_IF_loops;

public class ForLoop1ConditionalStatemen {

	public static void main(String[] args) {
		 
       for(int i=1; i<11; i++) {
    	   System.out.println("i := " + i);
	}

       for(int i=10; i>0; i--) {
    	   System.out.println("i := " + i);
       }
}
}