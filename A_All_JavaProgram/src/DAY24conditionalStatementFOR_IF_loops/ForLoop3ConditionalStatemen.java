package DAY24conditionalStatementFOR_IF_loops;

public class ForLoop3ConditionalStatemen {

	public static void main(String[] args) {
		 
		for(char c1='a'; c1<='z'; c1++) {
			System.out.println(c1);
	}
		
		for(char c1='A'; c1<='Z';c1++) {
			System.out.println(c1);
		}

		for (char c1='z';c1>='a'; c1--) {
			System.out.println(c1);
		}
}
}